    private void jButton_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_guardarActionPerformed
        String rol = "";
        rol = jComboBox_rol.getSelectedItem().toString().trim();
        if (txt_nombre.getText().isEmpty() || txt_apellido.getText().isEmpty() || txt_usuario.getText().isEmpty() || txt_contrasena.getText().isEmpty() || txt_telefono.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Completa todos los campos");
        } else {
            if (rol.equals("Seleccione rol:")) {
                JOptionPane.showMessageDialog(null, "Debe escoger un rol");
            } else {
                Usuario usuario = new Usuario();
                Ctrl_Usuario controlUsuario = new Ctrl_Usuario();
                if (!controlUsuario.existeUsuario(txt_usuario.getText().trim())) {
                    usuario.setNombre(txt_nombre.getText().trim());
                    usuario.setApellido(txt_apellido.getText().trim());
                    usuario.setUsuario(txt_usuario.getText().trim());
                    usuario.setContraseña(txt_contrasena.getText().trim());
                    usuario.setRol(rol);
                    usuario.setTelefono(txt_telefono.getText().trim());
                    usuario.setEstado(1);
                    if (controlUsuario.guardar(usuario)) {
                        JOptionPane.showMessageDialog(null, "Usuario registrado");
                        this.Limpiar();
                        this.jComboBox_rol.setSelectedItem("Seleccione rol:");
                    } else {
                        JOptionPane.showMessageDialog(null, "Error al registrar el usuario");
                    }

                } else {
                    JOptionPane.showMessageDialog(null, "El usuario ya se está registrado, ingrese otro");
                }
            }

        }


    }//GEN-LAST:event_jButton_guardarActionPerformed

