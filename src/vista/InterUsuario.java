    private void jCheckBox_ver_claveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jCheckBox_ver_claveMouseClicked

        if (jCheckBox_ver_clave.isSelected()==true) {
            String pass = "";
            char[] passIngresado = txt_contrasena.getPassword();
            for(int i=0; i< passIngresado.length; i++){
                pass += passIngresado[i];
            }
            txt_contrasena_visible.setText(pass);
            txt_contrasena.setVisible(false);
            txt_contrasena_visible.setVisible(true);
        } else {
            String contraIngresado = txt_contrasena_visible.getText().trim();
            txt_contrasena.setText(contraIngresado);
            txt_contrasena.setVisible(true);
            txt_contrasena_visible.setVisible(false);
        }
    }//GEN-LAST:event_jCheckBox_ver_claveMouseClicked


